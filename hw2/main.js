function numberbutton_click() {
  var ntitle = document.getElementById("numberpicture") ;
  var atitle = document.getElementById("alphabetpicture") ;
  if(ntitle.style.height == "0px") {
    ntitle.style.height = "auto" ;
  }
  else {
    ntitle.style.height = "0px" ;
  }
  atitle.style.height = "0px" ;
}

